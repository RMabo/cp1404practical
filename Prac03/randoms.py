"""random.py"""
import random
print(random.randint(5, 20))  # generating random int between the numbers 5 to 20
print(random.randrange(3, 10, 2))  # generating random int odd numbers 3 to 10
print(random.uniform(2.5, 5.5))  # generating random float numbers betweeen 2.5 to 5
print(random.randint(1, 100))
